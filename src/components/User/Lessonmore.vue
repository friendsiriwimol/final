          <h2>ความสำคัญของไก่พื้นเมือง</h2>
          <p style="text-indent: 2.5em;">ในอนาคตมีการคาดการณ์การเพิ่มขึ้นของประชากรโลก โดยคาดว่าในปี ค.ศ. 2050 จะมีจำนวนประชากรเพิ่มมากขึ้นถึง 9.3 พันล้านคน ภายใต้สภาวะการเปลี่ยนแปลงสภาพภูมิอากาศ การแข่งขันทรัพยากรธรรมชาติที่มีอยู่จำกัด รวมถึงการสูญเสียความหลากหลายทางชีวภาพของพืชและสัตว์ รวมถึงการเผชิญกับภาวะวิกฤติต่าง ๆ โลกจะต้องเผชิญกับปัญหาขาดแคลนอาหารอย่างยากจะหลีกเลี่ยง ปศุสตว์นับเป็นแหล่งอาหารโปรตีนสำคัญของมนุษย์ ผลผลิตจากเนื้อและไข่ของสัตว์ปีก โดยเฉพาะไก่นั้นมีบทบาทสำคัญในการเป็นแหล่งอาหารดังกล่าวที่ประชากรในทุกสถานะเข้าถึงได้ง่าย ในปัจจุบันมีการผลิตไก่เนื้อทางการค้าเพิ่มขึ้น เพื่อตอบสนองความต้องการของผู้บริโภคเนื้อไก่ที่เพิ่มขึ้น สำหรับไก่พื้นเมืองนั้นยังคงความสำคัญในบทบาทการเป็นทั้งแหล่งอาหารโปรตีน และเศรษฐกิจฐานรากของเกษตรกรรายย่อยในชุมชนชนบท ตลอดจนบทบาทด้านทรัพยากรพันธุกรรมที่สำคัญและคงความหลากหลายทางชีวภาพ (เทวินทร์, 2559) และปัจจุบันการเลี้ยงไก่พื้นเมืองมีโอกาสทางการตลาดที่เพิ่มขึ้น โดยเฉพาะในกลุ่มผู้บริโภคที่สนใจสุขภาพ ทั้งนี้มีรายงานในหลายประเทศจากกลุ่มสหภาพยุโรป เช่น ฝรั่งเศส เยอรมัน และโปแลนด์ ผู้บริโภคหันมาให้ความสำคัญกับการบริโภคเนื้อไก่พื้นเมืองเพิ่มมากขึ้น โดยเชื่อว่ามีไขมันน้อยกว่า และส่งผลดีต่อสุขภาพมากกว่าเนื้อประเภทอื่น ๆ (Salmon and Audran, 2016) สำหรับในประเทศไทย การบริโภคเนื้อไก่พื้นเมืองเป็นที่ได้รับความนิยมจากผู้บริโภคเพิ่มสูงขึ้น เนื่องจากมีรสชาติดี และเป็นอาหารที่เหมาะสำหรับผู้บริโภคที่ให้ความสำคัญกับอาหารสุขภาพ (healthy food) เนื่องจากเนื้อไก่พื้นเมืองมีคุณภาพเนื้อที่โดดเด่น รสชาติอร่อย เนื้อนุ่มแน่น และมีไขมันน้อย (หนึ่งฤทัย และคณะ, 2554; Promwatee et al., 2011) <br>
          ปัจจุบันการบริโภคเนื้อไก่พื้นเมืองคิดเป็น ร้อยละ 17.31 ของการบริโภคเนื้อไก่ทั้งหมดของไทย (กรมปศุสัตว์, 2558) (ภาพที่ 1)
          </p>
